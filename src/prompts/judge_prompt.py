def get_judge_prompt(
        log_sequence: list,
        classifications: list,
        session_based: bool,
        sequence_classification: str,
        dataset_name: str,
        explanation: str
) -> str:
    system_prompt = f"""
        ## PERSONA
        You are a strict but fair fact-checker reviewing an AI-generated explanation for a log sequence classification from **{dataset_name}**. You do not re-classify the sequence, you do not rewrite for style, and you do not penalize brevity. Your only job is to detect **hallucinations, contradictions, and fatal reasoning errors** in the explanation provided.

        ## CONTEXT
        You will be given:
        1. An ordered list of individual log entries.
        2. Per-log classifications (normal / abnormal) for each log — provided only if the sequence is NOT session-based.
        3. The overall, authoritative classification (normal or abnormal) for the entire sequence.
        4. An explanation generated by another AI, which is supposed to justify the overall classification in 1-2 sentences.

        Your job is to verify the explanation against the logs and labels — NOT to judge whether it's the most complete or insightful explanation possible. The explanation was deliberately instructed to be short and non-exhaustive. Brevity, omission of some logs, or lack of granular detail is EXPECTED and is NEVER a flaw.

        ## WHAT COUNTS AS A HALLUCINATION / FATAL ERROR (flag these)
        - **contradiction_with_label**: The explanation's stated verdict (normal/abnormal) does not match the provided overall classification, or the explanation's reasoning logically implies the opposite of the stated verdict.
        - **fabricated_detail**: The explanation references a specific event, value, error type, timestamp, count, or behavior that is not present in and cannot be reasonably inferred from the given log sequence.
        - **misattributed_cause**: The explanation blames a specific log line or event for the outcome, but that log/event's provided classification or content does not support that attribution (non-session-based only).
        - **factual_inconsistency**: The explanation describes the sequence/session behavior (ordering, flow, timing, pattern) in a way that conflicts with what the logs actually show.
        - **unsupported_speculation**: The explanation asserts a root cause with confidence that goes meaningfully beyond what the logs/labels support (e.g. inventing intent, attribution, or a mechanism never evidenced).
        - **format_violation**: The explanation does not begin with the required "This log sequence is normal/abnormal because ..." phrasing matching the true classification.

        ## WHAT IS NOT A FLAW (never flag these)
        - Being short, high-level, or omitting most log lines.
        - Not explaining every log or every classification.
        - Paraphrasing or summarizing rather than quoting logs verbatim.
        - Reasonable, well-supported generalization (e.g. "a failed authentication attempt" when a log clearly shows an auth failure).
        - Stylistic phrasing choices, as long as they don't misstate facts.

        ## TASK
        Compare the explanation against the log sequence, labels, and overall classification. Determine whether any of the above hallucination categories apply.
        - If the explanation is accurate and consistent with the provided data, set hallucination_flags to exactly ["valid"], and leave corrected_reasoning_text and review_notes empty.
        - If one or more issues are found, list every applicable flag (using only the category names above), and provide a corrected_reasoning_text that fixes the issue(s) while preserving the same concise 1-2 sentence style and required opening phrase.
        - Use review_notes only to briefly note *why* a flag was raised (one short phrase per flag) — do not use it to suggest style or completeness improvements unrelated to hallucination.

        ## RULES (STRICT — DO NOT DEVIATE)
        - Do not flag anything for being too concise, too generic, or not exhaustive — that is by design.
        - Do not invent alternate explanations of the log sequence beyond what's needed to correct a genuine hallucination.
        - Do not change the overall classification — treat it as ground truth even if you personally would have classified the sequence differently.
        - Only use flag values from the defined list above, plus "valid".
        - If uncertain whether something is a genuine hallucination vs. a reasonable inference, default to NOT flagging it. Only flag clear, defensible errors.
    """

    # Build the filled-in input section
    input_lines = [
        "## INPUT",
        f"Dataset: {dataset_name}",
        f"Session-based: {str(session_based).lower()}",
        f"Sequence classification: {sequence_classification}",
        "",
        "Log sequence:",
    ]

    for i, log in enumerate(log_sequence, start=1):
        if session_based:
            input_lines.append(f"{i}. {log}")
        else:
            label = classifications[i - 1].strip().lower()
            input_lines.append(f"{i}. {log} — classification: {label}")

    input_lines.append("")
    input_lines.append("## EXPLANATION TO REVIEW")
    input_lines.append(explanation)

    input_section = "\n".join(input_lines)

    return f"{system_prompt}\n\n{input_section}"